-- 7. ARTISTASDECANCION
-- Dado el ID de una canción, muestra todos los artistas que participan en ella según la tabla PERTENECE.

DELIMITER //

CREATE PROCEDURE ARTISTASDECANCION(IN ID INT)
BEGIN

    DECLARE EXISTE INT;
    DECLARE NOMCANCION VARCHAR(50);

    SELECT COUNT(*) INTO EXISTE FROM CANCION WHERE ID_C = ID;

    IF EXISTE = 0 THEN
        SELECT 'LA CANCION NO EXISTE' AS MENSAJE;
    ELSE
        SELECT NOMBRE INTO NOMCANCION FROM CANCION WHERE ID_C = ID;

        SELECT CONCAT('ARTISTAS QUE PARTICIPAN EN: ', NOMCANCION) AS INFO;

        SELECT A.NOMBRE AS ARTISTA FROM ARTISTA A, TIENE P WHERE A.ID_A = P.ID_A AND P.ID_C = ID;

    END IF;

END //

DELIMITER ;

CALL ARTISTASDECANCION(1);


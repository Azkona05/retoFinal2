(ID_A INT,
ID_C INT,
PRIMARY KEY(ID_A, ID_C),
FOREIGN KEY (ID_A) REFERENCES ARTISTA (ID_A) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (ID_C) REFERENCES CANCION (ID_C) ON DELETE CASCADE ON UPDATE CASCADE);


-- 4. CAMBIARNOMBREALBUM
-- Actualiza el nombre de un álbum dado su ID. Si el álbum no existe, lo indica.

DELIMITER //

CREATE PROCEDURE CAMBIARNOMBREALBUM(IN ID_ALB INT, IN NUEVO_NOM VARCHAR(50))
BEGIN
    DECLARE EXISTE INT;

    SELECT COUNT(*) INTO EXISTE FROM ALBUM WHERE ID_AL = ID_ALB;

    IF EXISTE = 0 THEN
        SELECT 'EL ALBUM NO EXISTE' AS MENSAJE;
    ELSE
        UPDATE ALBUM SET NOMBRE = NUEVO_NOM WHERE ID_AL = ID_ALB;
        SELECT CONCAT('ALBUM ACTUALIZADO A: ', NUEVO_NOM) AS MENSAJE;
    END IF;
END //


CREATE PROCEDURE obtenerCancionesPorArtista(IN p_id_artista INT)
BEGIN
    SELECT c.ID_C, c.NOMBRE, c.GENERO, al.NOMBRE AS nombre_album
    FROM CANCION c
    JOIN ALBUM al ON c.ID_AL = al.ID_AL
    WHERE al.ID_A = p_id_artista;
END //


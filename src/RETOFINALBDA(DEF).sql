-- 1. ALBUMSDEARTISTA
-- Muestra todos los álbumes que tiene un artista dado su ID, listándolos numerados. Si no existe o no tiene álbumes, avisa.

DELIMITER //

CREATE PROCEDURE ALBUMSDEARTISTA (IN ID INT)
BEGIN
    DECLARE NOM VARCHAR(50);
    DECLARE FIN BOOL DEFAULT 0;
    DECLARE N INT;
    DECLARE CONT INT DEFAULT 0;
    DECLARE C CURSOR FOR SELECT NOMBRE FROM ALBUM WHERE ID_A = ID;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET FIN = 1;
    
    SELECT COUNT(*) INTO N FROM ARTISTA WHERE ID_A = ID;

    IF N = 0 THEN
        SELECT 'El artista no existe' AS MENSAJE;
    ELSE
        OPEN C;
        FETCH C INTO NOM;

        WHILE FIN = 0 DO
            SET CONT = CONT + 1;
            SELECT CONCAT('ALBUM ', CONT, ': ', NOM) AS RESULTADO;
            FETCH C INTO NOM;
        END WHILE;

        CLOSE C;

        IF CONT = 0 THEN
            SELECT CONCAT('EL ARTISTA ', ID, ' NO TIENE ALBUMES') AS MENSAJE;
        END IF;
    END IF;
END //

DELIMITER ;

CALL ALBUMSDEARTISTA(1);

-- =====================================================
-- 2. CANCIONESDEALBUM
-- Dado el ID de un álbum, muestra cuántas canciones tiene y las lista. Si el álbum no existe o está vacío, avisa.

DELIMITER //

CREATE PROCEDURE CANCIONESDEALBUM (IN ID INT)
BEGIN
    DECLARE NOM VARCHAR(50);
    DECLARE N INT;
    DECLARE ENCONTRADO BOOL DEFAULT 1;

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET ENCONTRADO = 0;

    SELECT NOMBRE INTO NOM FROM ALBUM WHERE ID_AL = ID;

    IF ENCONTRADO = 0 THEN
        SELECT 'EL ALBUM NO EXISTE' AS MENSAJE;
    ELSE
        SELECT COUNT(*) INTO N FROM CANCION WHERE ID_AL = ID;

        IF N = 0 THEN
            SELECT CONCAT('EL ALBUM ', NOM, ' NO TIENE CANCIONES') AS RESUMEN;
        ELSE
            SELECT CONCAT('EL ALBUM ', NOM, ' TIENE ', N, ' CANCION(ES)') AS RESUMEN;

            SELECT NOMBRE AS CANCIONES FROM CANCION WHERE ID_AL = ID;
        END IF;

    END IF;

END //

DELIMITER ;
CALL CANCIONESDEALBUM(1);

-- =====================================================
-- 3. PROMEDIOCANCIONES
-- Calcula el promedio de canciones por álbum de un artista, mostrando el detalle acumulado álbum a álbum.

DELIMITER //

CREATE PROCEDURE PROMEDIOCANCIONES (IN ID INT)
BEGIN
    DECLARE NOM VARCHAR(50);
    DECLARE ALB INT;
	DECLARE N INT;
    DECLARE FIN BOOL DEFAULT 0;
    DECLARE CANC INT DEFAULT 0;
    DECLARE CONT INT DEFAULT 0;
    DECLARE PROM DECIMAL(5,2); 
    DECLARE C CURSOR FOR SELECT ID_AL, NOMBRE FROM ALBUM WHERE ID_A = ID;
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET FIN = 1;

    SELECT COUNT(*) INTO N FROM ARTISTA WHERE ID_A = ID;

    IF N = 0 THEN
        SELECT 'EL ARTISTA NO EXISTE' AS MENSAJE;
    ELSE
        OPEN C;
        FETCH C INTO ALB, NOM;

        WHILE FIN = 0 DO
            SET CANC = CANC + (SELECT COUNT(*) FROM CANCION WHERE ID_AL = ALB);
            SET CONT = CONT + 1;
            SELECT CONCAT('ALBUM ', CONT, ': ', NOM, ' -> Canciones acumuladas: ', CANC) AS DETALLE;
            FETCH C INTO ALB, NOM;
        END WHILE;

        CLOSE C;

        IF CONT = 0 THEN
            SELECT CONCAT('EL ARTISTA ', ID, ' NO TIENE ALBUMES') AS MENSAJE;
        ELSE
			SET PROM = CANC / CONT;
            SELECT CONCAT('PROMEDIO DE CANCIONES POR ALBUM: ', PROM) AS RESULTADO;
        END IF;
    END IF;
END //

DELIMITER ;

CALL PROMEDIOCANCIONES(1);


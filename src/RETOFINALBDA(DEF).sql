-- 6. NIVELARTISTA 
-- Devuelve el nivel de productividad de un artista según el total de canciones que tiene publicadas.

DELIMITER //

CREATE FUNCTION NIVELARTISTA(ID INT) RETURNS VARCHAR(50) DETERMINISTIC
BEGIN
    DECLARE TOTAL INT;
    DECLARE MENSAJE VARCHAR(50);

    SELECT COUNT(*) INTO TOTAL FROM CANCION C, ALBUM A WHERE C.ID_AL = A.ID_AL AND A.ID_A = ID;

    IF TOTAL >= 20 THEN
        SET MENSAJE = 'ARTISTA MUY PRODUCTIVO';
    ELSEIF TOTAL >= 10 THEN
        SET MENSAJE = 'ARTISTA PRODUCTIVO';
    ELSEIF TOTAL > 0 THEN
        SET MENSAJE = 'ARTISTA CON POCAS CANCIONES';
    ELSE
        SET MENSAJE = 'ARTISTA SIN CANCIONES';
    END IF;

    RETURN MENSAJE;

END //

DELIMITER ;

SELECT NIVELARTISTA(1);


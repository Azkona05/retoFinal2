-- 5. TIPOALBUM
-- Devuelve si un álbum es EP (8 canciones o menos) o LP (más de 8 canciones).

DELIMITER //

CREATE FUNCTION TIPOALBUM(ID INT) RETURNS VARCHAR(50) DETERMINISTIC
BEGIN
    DECLARE N INT;
    DECLARE MENSAJE VARCHAR(50);

    SELECT COUNT(*) INTO N FROM CANCION WHERE ID_AL = ID;

    IF N > 8 THEN
        SET MENSAJE = 'EL ALBUM ES UN LP';
    ELSE
        SET MENSAJE = 'EL ALBUM ES UN EP';
    END IF;

    RETURN MENSAJE;

END //

DELIMITER ;

SELECT TIPOALBUM(1);


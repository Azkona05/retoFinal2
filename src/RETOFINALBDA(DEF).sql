-- 8. GENEROPREDOMINANTE 
-- Devuelve el género musical más frecuente entre las canciones de un álbum dado su ID.

DELIMITER //

CREATE FUNCTION GENEROPREDOMINANTE(ID INT) RETURNS VARCHAR(20) DETERMINISTIC
BEGIN

    DECLARE R INT;
    DECLARE P INT;
    DECLARE REG INT;
    DECLARE T INT;
    DECLARE J INT;
    DECLARE RE INT;
    DECLARE MAYOR INT DEFAULT 0;
    DECLARE GENERO VARCHAR(20);

    SELECT COUNT(*) INTO R FROM CANCION WHERE ID_AL = ID AND GENERO = 'ROCK';
    SELECT COUNT(*) INTO P FROM CANCION WHERE ID_AL = ID AND GENERO = 'POP';
    SELECT COUNT(*) INTO REG FROM CANCION WHERE ID_AL = ID AND GENERO = 'REGGAETON';
    SELECT COUNT(*) INTO T FROM CANCION WHERE ID_AL = ID AND GENERO = 'TRAP';
    SELECT COUNT(*) INTO J FROM CANCION WHERE ID_AL = ID AND GENERO = 'JAZZ';
    SELECT COUNT(*) INTO RE FROM CANCION WHERE ID_AL = ID AND GENERO = 'REGGAE';

    SET MAYOR = R;
    SET GENERO = 'ROCK';

    IF P > MAYOR THEN
        SET MAYOR = P;
        SET GENERO = 'POP';
    END IF;

    IF REG > MAYOR THEN
        SET MAYOR = REG;
        SET GENERO = 'REGGAETON';
    END IF;

    IF T > MAYOR THEN
        SET MAYOR = T;
        SET GENERO = 'TRAP';
    END IF;

    IF J > MAYOR THEN
        SET MAYOR = J;
        SET GENERO = 'JAZZ';
    END IF;

    IF RE > MAYOR THEN
        SET MAYOR = RE;
        SET GENERO = 'REGGAE';
    END IF;
    
    IF MAYOR = 0 THEN
		RETURN 'SIN CANCIONES';
	END IF;

    RETURN GENERO;

END //

DELIMITER ;

SELECT GENEROPREDOMINANTE(1);

